package GenericsPractice;

public class ObjectContainer {
	private Object obj1;
	private Object obj2;
	public ObjectContainer(Object obj1, Object obj2) {
		this.obj1 = obj1;
		this.obj2 = obj2;
	}
	public Object getObj1(){
		return this.obj1;
	}
	public void setObj1(Object obj1) {
		this.obj1 = obj1;
	}
	public Object getObj2() {
		return this.obj2;
	}
	public void setObject2() {
		this.obj2 = obj2;
	}

}
